package rest.beans;

public class OperationResponse {
	
	private String result;

	public OperationResponse() {
		super();
	}

	public OperationResponse(String result) {
		super();
		this.result = result;
	}
	
	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

}
