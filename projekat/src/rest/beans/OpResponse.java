package rest.beans;

public class OpResponse {
	
	private String result;

	public OpResponse() {
		super();
	}

	public OpResponse(String result) {
		super();
		this.result = result;
	}
	
	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

}
